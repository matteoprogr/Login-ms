import java.util.UUID;

@Data
@NoArgsConstructor
@AllArgsConstructor
@Builder
public class UserResponse {

    private UUID id;
    private String username;
    private String email;

    @JsonProperty("is_active")
    private Boolean isActive;

    private Set<String> roles;
}
